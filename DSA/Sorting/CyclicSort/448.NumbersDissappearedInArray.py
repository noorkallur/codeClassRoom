def findDisappearedNumbers(nums):
    def swap(i , j):
        temp = nums[i]
        nums[i] = nums[j]
        nums[j] = temp
        
    i = 0
    while i < len(nums):
        if nums[i] != nums[ nums[i] -1 ]:
            swap(i, nums[i] -1)
        else:
            i+=1
            
    ans = list()
    i=0
    while i < len(nums):
        if nums[i] != i+1:
            ans.append(i+1)
        i+=1
    return ans
        
        
