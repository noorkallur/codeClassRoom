def maxNegativeNumber(arr):
    s = 0 
    e = len(arr)-1
    while(s < e):
        mid = (s+e)//2
        if arr[mid]<0 and arr[mid+1]>= 0:
            return mid
        elif arr[mid]<0:
            s = mid + 1
        else:
            e = mid -1
    return e

def leastPositiveNumber(arr):
    s = 0 
    e = len(arr)-1
    while(s < e):
        mid = (s+e)//2
        if arr[mid] <= 0:
            s = mid + 1
        else:
            e = mid
    return e

def maximumCount(nums):
    posNums = 0
    negNums = 0
    if nums[0] < 0:
        negNums= maxNegativeNumber(nums)
        negNums = negNums + 1
    if nums[len(nums)-1] > 0:
        posNums= leastPositiveNumber(nums)
        posNums = len(nums) - posNums
    return max(negNums, posNums)

