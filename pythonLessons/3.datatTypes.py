##Data types in Python are categorized into mutable and immutable data types.
# Mutable data type: is those whose values can be changed Eg. Lists, Dictionaries, Sets.
# immutable data type: is one in which the values can't be changed Eg. Integers, Floats, Strings, Tuples, FrozenSets

# List : ordered, mutable, and heterogeneous collection of objects.
# these objects are written within square brackets [ ]. this is how we identify if its a list or not.

# # lists:
# # lists are built into python, so you don’t need to import any module to use them.
# # lists can contain items of multiple data types, such as integers, strings, and even other lists.
# # accessing an element in a list is fast as elements are stored in contiguous memory locations.
# # inserting or deleting elements at the beginning or in the middle of a list can be inefficient due to the need to shift subsequent elements linearly.

# # arrays:
# # arrays are not built into python; you must import them from a module like ‘array’ or ‘numpy’.
# # arrays store elements of the same data type, so you can’t mix data types as in lists.
# # elements are stored in contiguous memory locations, ensuring fast access.
# # to create an array, you need to declare its data type and its initial size.

# # TUPLE:
# # A tuple in Python is a collection of ordered and immutable objects. Tuples are similar to lists, but they cannot be changed once they are created.
# # This makes them useful for storing data that should not be modified, such as database records.




